#' `r phylobase::nNodes(coccyzus_phylod)` internal
#' nodes.
#' @source Valente, L.M., A.B. Phillimore and R.S. Etienne (2015). Equilibrium
#' and non-equilibrium dynamics simultaneously operate in the Galapagos
